# given a dictionary whose values are all lists, prints the name of each key along with the size of its list, and then prints the associated values within each key's list
# output:
# 7 LOCATIONS
# San Jose
# Seattle
# Dallas
# Chicago
# Tulsa
# DC
# Burbank
    
# 8 INSTRUCTORS
# Michael
# Amy
# Eduardo
# Josh
# Graham
# Patrick
# Minh
# Devon
dojo = {
    'locations': ['San Jose', 'Seattle', 'Dallas', 'Chicago', 'Tulsa', 'DC', 'Burbank'],
    'instructors': ['Michael', 'Amy', 'Eduardo', 'Josh', 'Graham', 'Patrick', 'Minh', 'Devon']
}
def printInfo(someDict):
    for key in someDict:
        print(f"{len(someDict[key])} {key.upper()}")
        for i in someDict[key]:
            print(i)
        print("\n")
printInfo(dojo)
